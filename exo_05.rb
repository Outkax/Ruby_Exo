# #{} donne le résultat des données mise dedans
puts "On va compter le nombre d'heures de travail à THP"# Affiche la phrase
puts "Travail : #{10 * 5 * 11}"# Donne le résultat (ici le temps de travail) du calcule entre {}
puts "En minutes ça fait : #{10 * 5 * 11 * 60}"# Même chose en minutes

puts "Et en secondes ?"# Affiche la phrase

puts 10 * 5 * 11 * 60 * 60# Même chose en secondes

puts "Est-ce que c'est vrai que 3 + 2 < 5 - 7 ?"# Affiche la phrase

puts 3 + 2 < 5 - 7# Retourne la réponse ici false

puts "Ça fait combien 3 + 2 ? #{3 + 2}"# Donne le résultat de 3+2
puts "Ça fait combien 5 - 7 ? #{5 - 7}"# Donne le résultat de 5-7

puts "Ok, c'est faux alors !"# Affiche la phrase

puts "C'est drôle ça, faisons-en plus :"# Affiche la phrase

puts "Est-ce que 5 est plus grand que -2 ? #{5 > -2}"# Donne le résultat de 5 > -2
